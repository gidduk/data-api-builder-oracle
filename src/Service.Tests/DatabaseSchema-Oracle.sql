CREATE OR REPLACE TRIGGER trg_default_with_function_table_random
BEFORE INSERT ON default_with_function_table
FOR EACH ROW
BEGIN
    IF :NEW.random_number IS NULL THEN
        :NEW.random_number := DBMS_RANDOM.VALUE;
    END IF;
END;
